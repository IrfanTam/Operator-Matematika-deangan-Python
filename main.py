from fungsi import jumlah, kurang, kali, bagi

a = 2
b = 3

out1 = jumlah(a,b)
out2 = kurang(a,b)
out3 = kali(a,b)
out4 = bagi(a,b)

print(f'Hasil penjumlah dari {a} + {b} = {out1}')
print(f'Hasil penurangan dari {a} - {b} = {out2}')
print(f'Hasil perkalian dari {a} x {b} = {out3}')
print(f'Hasil perkalian dari {a} : {b} = {out4}')